package com.example.patterndesign;

import java.util.LinkedHashMap;

public class Line {

    public int x_from;
    public int y_from;
    public int x_to;
    public int y_to;

    public Line() {
    }

    public Line(int x_from, int y_from, int x_to, int y_to) {
        this.x_from = x_from;
        this.y_from = y_from;
        this.x_to = x_to;
        this.y_to = y_to;
    }


}